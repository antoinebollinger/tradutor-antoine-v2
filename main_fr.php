			<h1>- Mon travail -</h1>
			<h3>Ce que je fais</h3>
			<p>Je suis <strong>traducteur professionnel du portugais vers le français</strong>. Je m'occupe donc de traduire une grande variété de <strong>documents écrits en langue portugaise</strong> (du Portugal, du Brésil et d'ailleurs) <strong>vers le français</strong>, ma langue maternelle.</p>
			<p>Je suis aussi <strong>correcteur de texte</strong>. On me contacte régulièrement pour corriger des textes écrits en français mais pour lesquels l'auteur a besoin d'un œil professionnel, sûr et habitué à détecter toute sorte d'erreurs (grammaire, orthographe bien sûr, mais aussi et surtout syntaxe).</p>
			<h3>Pourquoi moi ?</h3>
			<p>Pourquoi un traducteur professionnel, quand il existe une quantité d'outils informatiques hyper performants pour traduire les textes en un clic ?</p>
			<p>C'est vrai, aujourd'hui de nombreuses plateformes comme Google, Reverso ou autres, vous permettent de traduire très facilement et gratuitement. Et il faut bien reconnaitre qu'ils sont très performants.</p>
			<p>Mais je dois quand même prêcher pour mon église ! Alors je dirais ceci : Traduire, ça n'est pas simplement prendre les mots un par un, les transposer dans une autre langue et les aligner. <strong>Traduire, c'est aussi capter le contexte</strong>, le type du document, d'où il vient, à qui il s'adresse, quel vocabulaire est employé dans la langue source et quel vocabulaire sera le mieux adapté dans la langue cible. <strong>Un même texte pourra être traduit de mille manières. L'enjeu est de trouver celle qui sera la plus adaptée à la destination du texte</strong>. Et ça, Google ne le fait pas encore...</p>
			<h3>Comment je le fais</h3>
			<p>Je m'efforce de travailler en respectant le principe énoncé plus haut, à savoir <strong>traduire et adapter</strong>. Au fil des années (ça commence à en faire !) j'ai su affiner ma maitrise du français, base de mon travail, ce qui me permet aujourd'hui de traduire fidèlement mais aussi dans un français adapté au type de document.</p>
			<p>J'ai aussi enrichi ma connaissance du portugais, à travers la multitude de projet que j'ai déjà mené (et il y en a eu beaucoup, dans de nombreux domaines), ce qui me permet de capter sûrement le contexte du document et d'être le plus efficace possible.</p>
			<div style="text-align:right;">	
			<br/>
			<h3>Ce que je ne fais pas</h3>
			<p>Je voulais vous parlez ici de deux choses importantes qu'on me demande souvent : la première, <strong>je ne suis pas traducteur assermenté</strong>, je ne peux donc pas traduire des documents officiels ; la seconde : <strong>je ne suis pas interprète</strong>, seulement traducteur.</p>
