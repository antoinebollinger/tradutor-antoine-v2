			<h1>- Meu trabalho -</h1>
			<h3>O que faço</h3>
			<p>Eu sou <strong>tradutor profissional do português para o francês</strong>. Cuido então de traduzir uma grande variedade de <strong>documentos escritos em língua portuguesa</strong> (de Portugal, do Brasil e outros) <strong>para o francês</strong>, minha língua materna.</p>
			<p>Eu faço também <strong>correção de texto</strong>. Sou regularmente solicitado para corrigir textos escritos em francês mas para os quais os autores precisam de um olhar profissional, seguro e acostumado a ver todos os tipos de erros (gramática, ortografia, claro, mas também e especialmente sintaxe).</p>
			<h3>Porque eu?</h3>
			<p>Porque um tradutor profissional enquanto existe hoje uma riqueza de ferramentas informáticas altamente performantes para traduzir textos em um clique?</p>
			<p>É verdade, hoje muitas plataformas, como Google, Reverso ou outros, permitem que você traduza com muita facilidade e de graça. E devemos reconhecer que eles são muito bem sucedidos.</p>
			<p>Mas ainda tenho que pregar para minha igreja! Então, eu diria o seguinte: Traduzir não é apenas pegar as palavras uma por uma, traduzi-las para outro idioma e alinhá-las. <strong>Traduzir também significa capturar o contexto</strong>, o tipo de documento, de onde vem, para quem é endereçado, qual vocabulário é usado na linguagem fonte e qual vocabulário será melhor adaptar no idioma de destino. <strong>O mesmo texto pode ser traduzido de mil maneiras. O desafio é encontrar aquela que será mais adequada ao destino do texto</strong>. E isso, Google ainda não faz...</p>
			<h3>Como faço</h3>
			<p>eu tento sempre trabalhar respeitando o princípio enunciado acima, a saber, <strong>traduzir e adaptar</strong>. Ao longo dos anos (já faz alguns!) consegui aprimorar cada vez mais o meu francês, base do meu trabalho, o que me permite hoje traduzir com fidelidade, mas também para um francês adaptado ao tipo do documento.</p>
			<p>Também enriqueci meu conhecimento de português, através da multiplicidade de projetos que já realizei (e são muitos, em muitas áreas), o que me permite seguramente capturar o contexto do documento e ser o mais eficaz possível.</p>
			<div style="text-align:right;">	
			<br/>
			<h3>O que não faço</h3>
			<p>Eu queria falar aqui de duas coisas importantes : a primeira, <strong>não sou tradutor juramentado</strong>, por isso não posso traduzir documentos oficiais; segunda : <strong>também não sou intérprete</strong>, apenas tradutor de textos escritos.</p>
