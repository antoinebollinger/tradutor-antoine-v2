				<p>No entanto, <strong>alguns projetos podem receber tarifas especiais</strong> dependendo de dois critérios principais:
				<ul>
					<li><p><strong>O prazo</strong> : para prazos extremamente curtos, posso aplicar uma taxa chamada "de urgência";</p></li>
					<li><p><strong>O tamanho do projeto</strong> : quando maior o documento, mais desconto eu aplico.</p></li>
				</ul>
				<p>Para um orçamento personalizado (e gratuito, claro) de um trabalho de tradução / correção do seu texto, envie-me o mesmo por email.</p>
